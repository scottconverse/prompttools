@app.command()
def rules(
    format: str = typer.Option(
        "text", "--format",
        help="Output format: text or json.",
    ),
    category: Optional[str] = typer.Option(
        None, "--category",
        help=(
            "Filter by category: token, system, formatting, variable, "
            "pipeline, hallucination, security, smell, gate, plugin."
        ),
    ),
) -> None:
    """List all available lint rules."""
    from promptlint.rules.base import BasePipelineRule, BaseRule

    all_file_rules = get_all_rules()
    all_pipeline_rules = get_all_pipeline_rules()

    # Combine into a unified list of dicts
    rule_entries: list[dict[str, object]] = []

    for r in all_file_rules:
        rule_entries.append({
            "rule_id": r.rule_id,
            "name": r.name,
            "severity": r.default_severity.value,
            "fixable": getattr(r, "fixable", False),
            "category": _category_for_rule(r.rule_id),
        })

    for r in all_pipeline_rules:
        rule_entries.append({
            "rule_id": r.rule_id,
            "name": r.name,
            "severity": r.default_severity.value,
            "fixable": False,
            "category": _category_for_rule(r.rule_id),
        })

    # Sort by rule_id
    rule_entries.sort(key=lambda e: str(e["rule_id"]))

    # Filter by category
    if category is not None:
        cat_lower = category.strip().lower()
        rule_entries = [e for e in rule_entries if e["category"] == cat_lower]

    if format.strip().lower() == "json":
        _console.print(json.dumps(rule_entries, indent=2), highlight=False)
        return

    # Text format
    if not rule_entries:
        _console.print("No rules found.", highlight=False)
        return

    _console.print("[bold]Available rules:[/bold]\n")
    for e in rule_entries:
        fixable_tag = " [green](fixable)[/green]" if e["fixable"] else ""
        sev = str(e["severity"])
        if sev == "error":
            sev_styled = f"[bold red]{sev}[/bold red]"
        elif sev == "warning":
            sev_styled = f"[bold yellow]{sev}[/bold yellow]"
        else:
            sev_styled = f"[bold blue]{sev}[/bold blue]"

        _console.print(
            f"  {e['rule_id']}  {e['name']:<40s} {sev_styled}  "
            f"[dim]{e['category']}[/dim]{fixable_tag}"
        )


# ---------------------------------------------------------------------------
# init command
# ---------------------------------------------------------------------------

@app.command()
def init() -> None:
    """Scaffold a .promptlint.yaml with defaults in the current directory."""
    target = Path.cwd() / ".promptlint.yaml"
    if target.exists():
        _err_console.print(
            f"[yellow]{target} already exists. Remove it first to re-scaffold.[/yellow]"
        )
        raise typer.Exit(code=1)

    target.write_text(_default_config_yaml(), encoding="utf-8")
    _console.print(f"[green]Created {target}[/green]")


# ---------------------------------------------------------------------------
# init-pipeline command
# ---------------------------------------------------------------------------

@app.command(name="init-pipeline")
def init_pipeline() -> None:
    """Scaffold a .promptlint-pipeline.yaml by scanning for prompt files."""
    target = Path.cwd() / _PIPELINE_MANIFEST
    if target.exists():
        _err_console.print(
            f"[yellow]{target} already exists. Remove it first to re-scaffold.[/yellow]"
        )
        raise typer.Exit(code=1)

    content = _scaffold_pipeline_yaml(Path.cwd())
    target.write_text(content, encoding="utf-8")
    _console.print(f"[green]Created {target}[/green]")


