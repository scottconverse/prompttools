"""Typer CLI application for promptlint.

Provides ``check``, ``watch``, ``rules``, ``init``, and ``init-pipeline``
commands.  Registered as the ``promptlint`` entry point via pyproject.toml.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Optional

import typer
from rich.console import Console

from promptlint.config import load_config
from promptlint.core.engine import (
    get_all_pipeline_rules,
    get_all_rules,
    lint_file,
    lint_pipeline,
)
from promptlint.core.fixer import apply_fixes
from promptlint.core.parser import parse_file, parse_pipeline_manifest, parse_stdin
from promptlint.core.reporter import report
from promptlint.models import Severity

# ---------------------------------------------------------------------------
# App and console
# ---------------------------------------------------------------------------

app = typer.Typer(
    name="promptlint",
    help="Static analysis tool for LLM prompts.",
    add_completion=False,
    no_args_is_help=True,
)

_console = Console()
_err_console = Console(stderr=True)

# Prompt file extensions recognised when walking directories.
PROMPT_EXTENSIONS: frozenset[str] = frozenset({".txt", ".md", ".yaml", ".yml", ".json"})

_PIPELINE_MANIFEST = ".promptlint-pipeline.yaml"

# Category mapping: derive from rule_id prefix.
_RULE_ID_CATEGORY: dict[str, str] = {
    "PL00": "token",
    "PL01": "system",
    "PL02": "formatting",
    "PL03": "variable",
    "PL04": "pipeline",
    "PL05": "hallucination",
    "PL06": "security",
    "PL07": "smell",
    "PL08": "gate",
}


def _category_for_rule(rule_id: str) -> str:
    """Return the human-readable category for a rule ID."""
    prefix = rule_id[:4] if len(rule_id) >= 4 else rule_id
    return _RULE_ID_CATEGORY.get(prefix, "plugin")


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _collect_prompt_files(path: Path) -> list[Path]:
    """Recursively collect prompt files under *path*."""
    files: list[Path] = []
    for child in sorted(path.rglob("*")):
        if child.is_file() and child.suffix.lower() in PROMPT_EXTENSIONS:
            files.append(child)
    return files


def _load_baseline(baseline_path: Path) -> set[tuple[str, str, int | None, str]]:
    """Load a baseline JSON report and return a set of violation keys."""
    try:
        data = json.loads(baseline_path.read_text(encoding="utf-8"))
    except Exception as exc:
        _err_console.print(f"[red]Error reading baseline: {exc}[/red]")
        raise typer.Exit(code=2) from exc

    keys: set[tuple[str, str, int | None, str]] = set()
    if isinstance(data, list):
        for item in data:
            keys.add((
                item.get("rule_id", ""),
                item.get("path", ""),
                item.get("line"),
                item.get("message", ""),
            ))
    return keys


def _default_config_yaml() -> str:
    """Return the default ``.promptlint.yaml`` scaffold content."""
    return """\
# promptlint configuration
# Docs: https://github.com/promptlint/promptlint

# Model profile (auto-sets context window + tokenizer)
# model: gpt-4

# Tokenizer
tokenizer:
  encoding: cl100k_base

# Token budget thresholds
token_budget:
  warn_threshold: 2048
  error_threshold: 4096
  system_prompt_threshold: 1024
  stop_word_ratio: 0.60

# Formatting
formatting:
  max_line_length: 500
  repetition_threshold: 3

# Per-rule severity overrides (rule_id: error|warning|info|ignore)
rules: {}

# Globally ignored rules
ignore: []

# File glob patterns to exclude
exclude: []

# Plugin directories
plugins: []
"""


def _scaffold_pipeline_yaml(directory: Path) -> str:
    """Scan *directory* for prompt files and produce a pipeline manifest."""
    files = _collect_prompt_files(directory)
    stages: list[str] = []
    for i, f in enumerate(files):
        rel = f.relative_to(directory)
        name = f.stem.replace(" ", "-").lower()
        entry = f"  - name: {name}\n    file: {rel}"
        if i > 0:
            prev_name = files[i - 1].stem.replace(" ", "-").lower()
            entry += f"\n    depends_on:\n      - {prev_name}"
        stages.append(entry)

    stages_block = "\n".join(stages) if stages else "  []"
    return f"""\
# promptlint pipeline manifest
# Generated by: promptlint init-pipeline

name: my-pipeline

stages:
{stages_block}
"""


