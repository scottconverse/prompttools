@app.command()
def watch(
    path: str = typer.Argument(
        ...,
        help="File or directory to watch.",
    ),
    config: Optional[Path] = typer.Option(
        None, "--config", "-c",
        help="Path to .promptlint.yaml config file.",
        exists=True, dir_okay=False,
    ),
    min_severity: str = typer.Option(
        "warning", "--min-severity",
        help="Minimum severity to report during watch.",
    ),
    clear: bool = typer.Option(
        True, "--clear/--no-clear",
        help="Clear terminal before each re-lint.",
    ),
) -> None:
    """Monitor files for changes and re-lint on save."""
    try:
        from watchfiles import watch as watchfiles_watch
    except ImportError:
        _err_console.print(
            "[red]watchfiles is required for watch mode. "
            "Install it with: pip install watchfiles[/red]"
        )
        raise typer.Exit(code=2)

    target = Path(path).resolve()
    if not target.exists():
        _err_console.print(f"[red]Path not found: {path}[/red]")
        raise typer.Exit(code=2)

    console = Console()

    # Load config once
    try:
        if config is not None:
            cfg = load_config(config.parent)
        else:
            cfg = load_config(target)
    except Exception as exc:
        _err_console.print(f"[red]Config error: {exc}[/red]")
        raise typer.Exit(code=2) from exc

    def _is_prompt_file(p: Path) -> bool:
        return p.suffix.lower() in PROMPT_EXTENSIONS

    def _lint_and_display(file_path: Path) -> None:
        """Lint a single file and display results."""
        if clear:
            console.clear()
        console.rule(f"[bold cyan]promptlint watch[/bold cyan] — {file_path.name}")
        try:
            prompt_file = parse_file(file_path)
            violations = lint_file(prompt_file, cfg, min_severity)
            output = report(violations, format="text", stats=False)
            console.print(output, highlight=False)
        except Exception as exc:
            console.print(f"[red]Error: {exc}[/red]")

    # Initial lint
    if target.is_file():
        _lint_and_display(target)
    else:
        console.print(
            f"[bold cyan]Watching[/bold cyan] {target} for changes...",
            highlight=False,
        )

    # Determine watch path
    watch_path = target if target.is_dir() else target.parent

    try:
        for changes in watchfiles_watch(watch_path):
            for _change_type, changed_path_str in changes:
                changed = Path(changed_path_str)
                if _is_prompt_file(changed) and changed.is_file():
                    _lint_and_display(changed)
    except KeyboardInterrupt:
        console.print("\n[bold cyan]Watch stopped.[/bold cyan]")
        raise typer.Exit(code=0)


