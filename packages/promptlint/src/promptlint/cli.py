@app.command()
def check(
    path: str = typer.Argument(
        ...,
        help="File or directory to lint. Use '-' to read from stdin.",
    ),
    config: Optional[Path] = typer.Option(
        None, "--config", "-c",
        help="Path to .promptlint.yaml config file.",
        exists=True, dir_okay=False,
    ),
    format: str = typer.Option(
        "text", "--format", "-f",
        help="Output format: text, json, github.",
    ),
    input_format: Optional[str] = typer.Option(
        None, "--input-format",
        help="Force input format for stdin: text, md, yaml, json.",
    ),
    model: Optional[str] = typer.Option(
        None, "--model", "-m",
        help="Model profile name (overrides config file).",
    ),
    select: Optional[str] = typer.Option(
        None, "--select",
        help="Comma-separated rule IDs to enable.",
    ),
    ignore: Optional[str] = typer.Option(
        None, "--ignore",
        help="Comma-separated rule IDs to suppress.",
    ),
    min_severity: str = typer.Option(
        "info", "--min-severity",
        help="Minimum severity to report: info, warning, error.",
    ),
    no_color: bool = typer.Option(
        False, "--no-color",
        help="Disable rich color output.",
    ),
    quiet: bool = typer.Option(
        False, "--quiet", "-q",
        help="Only print errors, suppress warnings and info.",
    ),
    stats: bool = typer.Option(
        False, "--stats",
        help="Print a rule-hit summary table after violations.",
    ),
    fix: bool = typer.Option(
        False, "--fix",
        help="Apply auto-fixes for fixable violations.",
    ),
    dry_run: bool = typer.Option(
        False, "--dry-run",
        help="With --fix: preview fixes without writing.",
    ),
    pipeline: bool = typer.Option(
        False, "--pipeline",
        help="Treat directory as a pipeline (requires manifest).",
    ),
    baseline: Optional[Path] = typer.Option(
        None, "--baseline",
        help="Path to a previous JSON report; only show new violations.",
        exists=True, dir_okay=False,
    ),
    cache: bool = typer.Option(
        False, "--cache",
        help="Enable token count caching.",
    ),
) -> None:
    """Lint one or more prompt files or directories."""
    console = Console(no_color=no_color)

    # Override min_severity when --quiet
    effective_min_severity = "error" if quiet else min_severity

    # Build CLI overrides dict
    cli_overrides: dict[str, object] = {}
    if model is not None:
        cli_overrides["model"] = model
    if ignore is not None:
        cli_overrides["ignore"] = ignore
    if select is not None:
        cli_overrides["select"] = [r.strip() for r in select.split(",")]

    # ------------------------------------------------------------------
    # Resolve target path
    # ------------------------------------------------------------------
    reading_stdin = path == "-"

    if reading_stdin:
        # Read from stdin
        try:
            stdin_content = sys.stdin.read()
        except Exception as exc:
            _err_console.print(f"[red]Error reading stdin: {exc}[/red]")
            raise typer.Exit(code=2) from exc

        fmt = input_format or "text"
        try:
            prompt_file = parse_stdin(stdin_content, fmt)
        except Exception as exc:
            _err_console.print(f"[red]Parse error: {exc}[/red]")
            raise typer.Exit(code=2) from exc

        cfg = load_config(Path.cwd(), cli_overrides or None)
        try:
            violations = lint_file(prompt_file, cfg, effective_min_severity)
        except Exception as exc:
            _err_console.print(f"[red]Lint error: {exc}[/red]")
            raise typer.Exit(code=2) from exc

        output = report(violations, format=format, stats=stats)
        if output:
            console.print(output, highlight=False)
        raise typer.Exit(code=1 if violations else 0)

    # Non-stdin: resolve the path on disk
    target = Path(path).resolve()
    if not target.exists():
        _err_console.print(f"[red]Path not found: {path}[/red]")
        raise typer.Exit(code=2)

    # Load config (use explicit --config or auto-discover)
    try:
        if config is not None:
            # Read the explicit config, then apply CLI overrides
            cfg = load_config(config.parent, cli_overrides or None)
        else:
            cfg = load_config(target, cli_overrides or None)
    except Exception as exc:
        _err_console.print(f"[red]Config error: {exc}[/red]")
        raise typer.Exit(code=2) from exc

    # Determine selected rules (for filtering)
    selected_ids: set[str] | None = None
    if select is not None:
        selected_ids = {r.strip() for r in select.split(",")}

    # ------------------------------------------------------------------
    # Pipeline mode
    # ------------------------------------------------------------------
    if pipeline:
        manifest_path: Path | None = None
        if target.is_file() and target.name == _PIPELINE_MANIFEST:
            manifest_path = target
        elif target.is_dir():
            candidate = target / _PIPELINE_MANIFEST
            if candidate.is_file():
                manifest_path = candidate

        if manifest_path is None:
            _err_console.print(
                f"[red]Pipeline manifest ({_PIPELINE_MANIFEST}) not found.[/red]"
            )
            raise typer.Exit(code=2)

        try:
            pipeline_obj = parse_pipeline_manifest(manifest_path)
        except Exception as exc:
            _err_console.print(f"[red]Pipeline parse error: {exc}[/red]")
            raise typer.Exit(code=2) from exc

        try:
            violations = lint_pipeline(pipeline_obj, cfg, effective_min_severity)
        except Exception as exc:
            _err_console.print(f"[red]Lint error: {exc}[/red]")
            raise typer.Exit(code=2) from exc

        # Filter by --select
        if selected_ids is not None:
            violations = [v for v in violations if v.rule_id in selected_ids]

        # Apply baseline filtering
        if baseline is not None:
            baseline_keys = _load_baseline(baseline)
            violations = [
                v for v in violations
                if (v.rule_id, str(v.path), v.line, v.message) not in baseline_keys
            ]

        # Auto-fix
        if fix:
            all_rules_list = get_all_rules(cfg)
            fix_lines = apply_fixes(violations, all_rules_list, dry_run=dry_run)
            for line in fix_lines:
                console.print(line, highlight=False)

        output = report(violations, format=format, stats=stats)
        if output:
            console.print(output, highlight=False)
        raise typer.Exit(code=1 if violations else 0)

    # ------------------------------------------------------------------
    # Single file or directory mode
    # ------------------------------------------------------------------
    files_to_lint: list[Path] = []
    if target.is_file():
        files_to_lint.append(target)
    elif target.is_dir():
        files_to_lint = _collect_prompt_files(target)
        if not files_to_lint:
            _err_console.print(
                f"[yellow]No prompt files found in {target}[/yellow]"
            )
            raise typer.Exit(code=0)
    else:
        _err_console.print(f"[red]Path is neither a file nor directory: {path}[/red]")
        raise typer.Exit(code=2)

    all_violations = []
    for file_path in files_to_lint:
        try:
            prompt_file = parse_file(file_path)
        except Exception as exc:
            _err_console.print(
                f"[red]Parse error in {file_path}: {exc}[/red]"
            )
            continue

        try:
            file_violations = lint_file(prompt_file, cfg, effective_min_severity)
        except Exception as exc:
            _err_console.print(
                f"[red]Lint error in {file_path}: {exc}[/red]"
            )
            continue

        all_violations.extend(file_violations)

    # Filter by --select
    if selected_ids is not None:
        all_violations = [v for v in all_violations if v.rule_id in selected_ids]

    # Apply baseline filtering
    if baseline is not None:
        baseline_keys = _load_baseline(baseline)
        all_violations = [
            v for v in all_violations
            if (v.rule_id, str(v.path), v.line, v.message) not in baseline_keys
        ]

    # Auto-fix
    if fix:
        all_rules_list = get_all_rules(cfg)
        fix_lines = apply_fixes(all_violations, all_rules_list, dry_run=dry_run)
        for line in fix_lines:
            console.print(line, highlight=False)

    output = report(all_violations, format=format, stats=stats)
    if output:
        console.print(output, highlight=False)
    raise typer.Exit(code=1 if all_violations else 0)


